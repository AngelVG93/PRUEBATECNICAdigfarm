use pruebatecnicaadigfarm;
CREATE TABLE TipoEmpledo (
    idTipoEmpleado int NOT NULL IDENTITY(1,1) PRIMARY KEY,
	nombre varchar(9) not null
);

CREATE TABLE TipoNovedad (
    idTipoNovedad int NOT NULL IDENTITY(1,1) PRIMARY KEY,
	nombre varchar(22)not null
);

CREATE TABLE Empleado (
    idEmpleado int NOT NULL IDENTITY(1,1) PRIMARY KEY,
    nombre varchar(150)not null,
    cedula numeric(18,0)not null,
    estado bit not null,
    horaEntrada datetime not null,
	horaSalida datetime not null,
	idTipoEmpleado int not null,
	CONSTRAINT Empleado_TipoEmpledo_fk FOREIGN KEY (idTipoEmpleado) REFERENCES TipoEmpledo(idTipoEmpleado)
);

CREATE TABLE RegistroEntrada (
    idRegistroEntrada int NOT NULL IDENTITY(1,1) PRIMARY KEY,
    idTipoNovedad int not null,
	idEmpleado int not null,
	horaEntrada datetime,
	horaSalida datetime,
	fechaNovedad datetime not null,
	horas numeric(18,2)not null,
	descripcion varchar(250) not null,
	CONSTRAINT RegistroEntrada_TipoNovedad_fk FOREIGN KEY (idTipoNovedad) REFERENCES TipoNovedad(idTipoNovedad),
	CONSTRAINT RegistroEntrada_Empleado_fk FOREIGN KEY (idEmpleado) REFERENCES Empleado(idEmpleado)
);

